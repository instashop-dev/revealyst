// W1-S gate evidence generator — mechanically assembles the automated slice
// of docs/gates/W1-evidence.md (rule 4: the founder judges evidence, never
// code). Run by /gate-check W1 (or by hand) from the repo root:
//
//   node scripts/gate-evidence.mjs
//
// It runs typecheck + the org-scope guard + the full vitest suite (JSON
// reporter), extracts the harness-owned proof points (contract tests,
// tenant-isolation sweep, ingest→score E2E, recorded-fixture coverage,
// seam-registry state), and writes the pack with NEEDS-FOUNDER slots for
// everything that cannot be self-certified (live-data checks, screenshots,
// dogfooding comparison, adversarial findings — rule 4 keeps those human).

import { spawnSync } from "node:child_process";
import { existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const ROOT = process.cwd();
const OUT = join(ROOT, "docs", "gates", "W1-evidence.md");
const VITEST_JSON = join(ROOT, ".vitest-gate-report.json");

function run(label, command, args) {
  console.log(`\n== ${label}: ${command} ${args.join(" ")}`);
  const res = spawnSync(command, args, {
    cwd: ROOT,
    shell: true, // npm/npx are .cmd shims on Windows
    encoding: "utf8",
    maxBuffer: 64 * 1024 * 1024,
  });
  const output = `${res.stdout ?? ""}${res.stderr ?? ""}`;
  console.log(output.split("\n").slice(-6).join("\n"));
  return { ok: res.status === 0, output };
}

// --- 1. typecheck + org-scope guard -----------------------------------------
const typecheck = run("typecheck", "npm", ["run", "typecheck"]);
const orgScope = run("org-scope guard", "node", ["scripts/check-org-scope.mjs"]);

// --- 2. full suite with JSON reporter ---------------------------------------
const vitest = run("vitest", "npx", [
  "vitest",
  "run",
  "--reporter=default",
  "--reporter=json",
  `--outputFile=${VITEST_JSON}`,
]);

let report = null;
if (existsSync(VITEST_JSON)) {
  report = JSON.parse(readFileSync(VITEST_JSON, "utf8"));
}

function suite(nameFragment) {
  const files = (report?.testResults ?? []).filter((t) =>
    t.name.replaceAll("\\", "/").includes(nameFragment),
  );
  if (files.length === 0) return { found: false, passed: false, tests: 0 };
  const tests = files.reduce((n, f) => n + f.assertionResults.length, 0);
  const passed = files.every(
    (f) =>
      f.status === "passed" &&
      f.assertionResults.every((a) => a.status === "passed"),
  );
  return { found: true, passed, tests };
}

const totals = {
  files: report?.testResults?.length ?? 0,
  tests: report?.numTotalTests ?? 0,
  passed: report?.numPassedTests ?? 0,
  failed: report?.numFailedTests ?? 0,
};
const contracts = suite("tests/contracts.test.ts");
const isolation = suite("tests/tenant-isolation.test.ts");
const e2e = suite("tests/e2e/ingest-to-score.e2e.test.ts");
const vendorFixtures = suite("tests/vendor-fixtures.test.ts");

// --- 3. recorded-fixture coverage + seam state -------------------------------
const payloadsDir = join(ROOT, "fixtures", "vendor-payloads");
const recordings = {};
for (const entry of readdirSync(payloadsDir, { withFileTypes: true })) {
  if (!entry.isDirectory()) continue;
  const n = readdirSync(join(payloadsDir, entry.name)).filter((f) =>
    f.endsWith(".json"),
  ).length;
  if (n > 0) recordings[entry.name] = n;
}
const recordingsLine =
  Object.keys(recordings).length === 0
    ? "**none committed yet** — founder runs `scripts/record/anthropic.mjs` with live keys"
    : Object.entries(recordings)
        .map(([v, n]) => `${v}: ${n} file(s)`)
        .join(" · ");

const seams = readFileSync(join(ROOT, "tests", "harness", "seams.ts"), "utf8");
const connectorSeam = seams.includes("referenceAnthropicConsole")
  ? "reference stub (W1-D not merged)"
  : "production connector";
const engineSeam = seams.includes("evaluateScore")
  ? "reference evaluator (W1-F not merged)"
  : "production engine";

// --- 4. write the pack --------------------------------------------------------
const pass = (ok) => (ok ? "PASS" : "**FAIL**");
const founder = (what) => `**NEEDS FOUNDER** — ${what}`;
const now = new Date().toISOString();

const md = `# W1 exit-gate evidence — ingest→score path

Generated by \`scripts/gate-evidence.mjs\` at ${now}. Automated sections only —
rule 4: the NEEDS-FOUNDER slots below are judged by a human against reality,
never self-certified by an authoring agent.

Suite state at generation: **${totals.passed}/${totals.tests} tests green across
${totals.files} files** (\`npm test\`) · typecheck ${pass(typecheck.ok)} ·
org-scope guard ${pass(orgScope.ok)} · vitest exit ${pass(vitest.ok)}.

## Exit-gate checklist (Execution Plan, Wave 1)

| # | Gate item | Evidence |
|---|---|---|
| 1 | Real Anthropic data lands normalized, attribution-tagged, backfilled, re-pollable (W1-D) | Seam: **${connectorSeam}**. ${founder("run the merged connector against live keys; recordings replay must match reality (scripts/record/README.md)")} |
| 2 | Claude Code local ingest pushes metric records (W1-E) | ${founder("run `npx revealyst-agent sync` against real local logs; confirm records land")} |
| 3 | Engine computes correct scores from fixtures (W1-F) | Seam: **${engineSeam}**. E2E hand-verified adoption preset: value 30, attribution key_project (${pass(e2e.passed)}) |
| 4 | App shell live behind auth (W1-G) | ${founder("sign in at the production URL; screenshot the authenticated shell")} |
| 5 | E2E harness green on ingest→score (W1-S) | ${pass(e2e.passed && e2e.found)} — tests/e2e/ingest-to-score.e2e.test.ts (${e2e.tests} tests): signup → connect → replay → normalize → upsert (idempotent) → score → isolation |
| 6 | Tenant-isolation sweep (every wave) | ${pass(isolation.passed && isolation.found)} — tests/tenant-isolation.test.ts (${isolation.tests} tests incl. completeness tripwire) |
| 7 | Contract tests — no drift from contracts-v1 | ${pass(contracts.passed && contracts.found)} — tests/contracts.test.ts (${contracts.tests} tests); frozen-contracts CI job active |
| 8 | Recorded vendor payloads committed & scrub-linted | ${pass(vendorFixtures.passed && vendorFixtures.found)} — tests/vendor-fixtures.test.ts (${vendorFixtures.tests} tests). Coverage: ${recordingsLine} |

## Test output (JSON reporter summary)

${(report?.testResults ?? [])
  .map((t) => {
    const rel = t.name.replaceAll("\\", "/").split("/").slice(-2).join("/");
    const n = t.assertionResults.length;
    return `- ${t.status === "passed" ? "PASS" : "**FAIL**"} ${rel} (${n})`;
  })
  .join("\n")}

## Known-truth dogfooding comparison

${founder("compare computed spend/usage for the founder org against the Anthropic Console dashboard for the same window; record the numbers and divergence %")}

## Adversarial pre-review findings

${founder("run /code-review ultra + /gate-review W1; paste findings with dispositions here")}

## Sign-off

- [ ] Do these numbers match reality?
- [ ] Would I trust this score?
`;

mkdirSync(join(ROOT, "docs", "gates"), { recursive: true });
writeFileSync(OUT, md);
console.log(`\nwrote ${OUT}`);
if (!typecheck.ok || !orgScope.ok || !vitest.ok) {
  console.error("gate-evidence: one or more automated checks FAILED — pack records the failure");
  process.exitCode = 1;
}
